"""EXP-064: the world-model compiler — one simulate(question) call, the right mechanism every time.

This is the keystone in action. Five DIFFERENT questions go through the SAME `WorldModel.simulate(question)`
call; the compiler reads each and emits the structural model whose MECHANISM matches the real generative
process — a bracket for a competition, a committee for an institution, an electorate for a population, a
generic SCM for coupled quantitative dynamics, a single agent for one person. The engine then rolls each
forward at calibrated time and returns a calibrated distribution with an honest reducible/irreducible split.

The specs here are committed (the cached backend), representing the LLM compiler's structural judgment for
reproducibility; production swaps in `anthropic_compile_fn` behind the identical interface. The point is
that ONE call handles all five, dispatching to the right generative process — the thing a single generic
mechanism could not do (and why the NBA composite failed).

Run: python -m experiments.exp064_world_model_compiler
"""
from __future__ import annotations

import json
from pathlib import Path

from swm.api.compiler import StructuralCompiler, cached_compile_fn
from swm.api.world_model import WorldModel

RESULT = "experiments/results/exp064_world_model_compiler.json"

# realistic NBA Elos (as EXP-063), expressed as a bracket spec the compiler would emit
_WEST = {"OKC Thunder": 1660, "Denver": 1600, "Houston": 1590, "Lakers": 1565, "Minnesota": 1555,
         "Clippers": 1520, "Warriors": 1505, "Grizzlies": 1490}
_EAST = {"Cleveland": 1620, "New York": 1555, "Orlando": 1520, "Milwaukee": 1515, "Boston": 1500,
         "Philadelphia": 1495, "Indiana": 1475, "Detroit": 1465}


def _nba_spec():
    comp = [{"name": t, "strength": e, "est_sd": 40} for t, e in {**_WEST, **_EAST}.items()]
    groups = {**{t: "west" for t in _WEST}, **{t: "east" for t in _EAST}}
    return {"mechanism": "bracket", "extra": {"competitors": comp, "groups": groups,
            "series_length": 7, "home_advantage": 100}, "outcome": {"target": "OKC Thunder"},
            "horizon": 1, "rationale": "a championship is generated by a playoff bracket of best-of-7 series"}


def _fomc_spec():
    hawks = [0.85, 0.8, 0.75]; mods = [0.35, 0.32, 0.30, 0.34, 0.28]; doves = [0.15, 0.12, 0.18]
    agents = ([{"id": "Chair", "position": 0.55, "influence": 2.0, "openness": 0.3, "conviction": 0.6}]
              + [{"id": f"hawk{i}", "position": h, "influence": 1.2, "openness": 0.3, "conviction": 0.6}
                 for i, h in enumerate(hawks)]
              + [{"id": f"mod{i}", "position": m, "influence": 1.0, "openness": 0.55, "conviction": 0.3}
                 for i, m in enumerate(mods)]
              + [{"id": f"dove{i}", "position": d, "influence": 1.0, "openness": 0.5, "conviction": 0.4}
                 for i, d in enumerate(doves)])
    return {"mechanism": "committee", "extra": {"agents": agents, "homophily": 0.4, "consensus_pull": 0.3,
            "rounds": 6, "position_sd": 0.06}, "outcome": {"event": {"op": ">", "value": 0.5}},
            "horizon": 1, "rationale": "a rate decision is a committee vote among FOMC members"}


def _referendum_spec():
    # a population by demographic segment; outcome = does support exceed 50%?
    cells = [{"id": "young_urban", "stance": 0.62, "weight": 25, "turnout": 0.55, "est_sd": 0.04},
             {"id": "older_rural", "stance": 0.38, "weight": 22, "turnout": 0.72, "est_sd": 0.04},
             {"id": "suburban_mid", "stance": 0.51, "weight": 30, "turnout": 0.65, "est_sd": 0.03},
             {"id": "college_prof", "stance": 0.58, "weight": 23, "turnout": 0.75, "est_sd": 0.04}]
    return {"mechanism": "electorate", "extra": {"cells": cells, "participation_weighted": True,
            "turnout_coupling": 0.5, "k_social": 0.1, "k_proof": 0.03},
            "outcome": {"event": {"op": ">", "value": 0.5}}, "horizon": 6,
            "rationale": "a referendum is a demographic population with turnout-weighted, coupled opinion"}


def _incumbent_spec():
    # coupled quantitative dynamics: vote pulled toward a fundamentals level over a 26-week campaign
    return {"mechanism": "generic_scm",
            "variables": [{"name": "economy", "value": 0.45, "est_sd": 0.03, "volatility": 0.01},
                          {"name": "approval", "value": 0.47, "est_sd": 0.03, "volatility": 0.02},
                          {"name": "vote", "value": 0.49, "est_sd": 0.01, "volatility": 0.015}],
            "equations": {"vote": "0.3*(0.5*approval + 0.35*economy + 0.075 - vote)"},
            "outcome": {"variable": "vote", "event": {"op": ">", "value": 0.5}},
            "horizon": 26, "dt": 1, "rationale": "vote share diffuses toward a fundamentals-implied level"}


def _email_spec():
    return {"mechanism": "single_agent",
            "extra": {"person": {"trait_openness": 0.6, "skepticism": 0.4, "goal_alignment": 0.55},
                      "message": {"clarity": 0.8, "ask_directness": 0.7, "personalization": 0.7,
                                  "expertise": 0.6}, "est_sd": {"trait_openness": 0.1, "skepticism": 0.1}},
            "outcome": {}, "horizon": 1, "rationale": "one person's reply to a specific email"}


QUESTIONS = {
    "Will OKC win the 2026 NBA championship?": _nba_spec(),
    "Will the FOMC raise the federal funds rate at its next meeting?": _fomc_spec(),
    "Will the ballot referendum pass?": _referendum_spec(),
    "Will the incumbent hold the seat in November?": _incumbent_spec(),
    "Will this person reply to my outreach email?": _email_spec(),
}


def run():
    compiler = StructuralCompiler(compile_fn=cached_compile_fn(QUESTIONS))
    wm = WorldModel(compiler=compiler, retriever=None)
    results = {}
    print("EXP-064  world-model compiler — ONE simulate(question) call, the right mechanism each time")
    for q in QUESTIONS:
        out = wm.simulate(q, key=q)
        results[q] = out
        f = out["forecast"]
        print(f"\n  Q: {q}")
        print(f"     mechanism compiled: {out['mechanism']}   (\"{out['spec']['rationale']}\")")
        if out["mechanism"] == "bracket":
            print(f"     champion favorite: {f['favorite']}  P(OKC)={f['p_target']} "
                  f"(={f['p_target_strengths_known']} even with strengths known -> rest is irreducible)")
        elif out["mechanism"] == "single_agent":
            print(f"     P(reply) ~ {f['p_respond_mean']}  80% interval {f['interval_80']}")
        else:
            key = "p_event"
            print(f"     {out['headline']}")
            if isinstance(f.get("uncertainty"), dict):
                u = f["uncertainty"]
                print(f"     uncertainty: irreducible_frac={u['irreducible_frac']} "
                      f"forecastable={u['forecastable']}")
    Path(RESULT).write_text(json.dumps(results, indent=1, default=str))
    print(f"\n  wrote {RESULT}")
    return results


if __name__ == "__main__":
    run()
