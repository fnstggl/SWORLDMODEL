_ADVERSARY_PROMPT = """BOUNDARY ADVERSARY. Below is a world boundary for a simulation. Everything is data.
QUESTION: {q}
CAUSAL THESIS: {thesis}
CURRENT BOUNDARY: individual={individual}; aggregates={aggregates}; external={external}; excluded={excluded}
Propose ONE plausible ALTERNATIVE world boundary that would produce a MATERIALLY DIFFERENT result:
which components move inside/outside, and why the answer would differ. Do NOT provide probabilities
or recommendations. Return STRICT JSON: {{"alternative_boundary": {{"moves_inside": ["..."],
"moves_outside": ["..."], "why_result_differs": "...", "sensitivity": "decisive|material|minor|negligible|unknown"}}}}"""


def run_boundary_critics(boundary: WorldBoundary, *, llm, thesis: str = "") -> list:
    """Run the three independent critics with actual LLM calls; append findings to the boundary
    (open resolution) and register omitted components as unresolved with the critic's
    sensitivity. Each critic call is traced. Returns the appended findings."""
    from swm.engine.grounding import parse_json
    if llm is None:
        boundary.generation_trace.append({"stage": "boundary_critics", "prompt_hash": "",
                                          "response_hash": "", "ok": False,
                                          "error": "no_llm_backend"})
        return []
    individual = ", ".join(boundary.included_individual_actors +
                           boundary.included_institutions)[:500] or "(none)"
    aggregates = ", ".join(boundary.represented_as_aggregates)[:300] or "(none)"
    external = ", ".join(boundary.represented_as_external_processes)[:300] or "(none)"
    excluded = ", ".join(e["name"] for e in boundary.explicitly_excluded)[:300] or "(none)"
    prompts = {
        "omitted_actor": _OMITTED_ACTOR_PROMPT.format(
            q=boundary.question[:500], thesis=(thesis or "(unstated)")[:400],
            individual=individual, aggregates=aggregates, external=external),
        "omitted_system": _OMITTED_SYSTEM_PROMPT.format(
            q=boundary.question[:500], thesis=(thesis or "(unstated)")[:400],
            systems=", ".join(boundary.included_nonhuman_systems)[:300] or "(none)",
            mechanisms=", ".join(boundary.included_mechanisms)[:300] or "(none)",
            external=external),
        "boundary_adversary": _ADVERSARY_PROMPT.format(
            q=boundary.question[:500], thesis=(thesis or "(unstated)")[:400],
            individual=individual, aggregates=aggregates, external=external, excluded=excluded),
    }
    appended = []
    for critic, prompt in prompts.items():
        try:
            txt = llm(prompt)
            raw = parse_json(txt) or {}
            ok = True
            err = ""
        except Exception as e:  # noqa: BLE001
            raw, ok, err = {}, False, f"{type(e).__name__}: {e}"[:160]
        boundary.generation_trace.append({"stage": f"critic:{critic}",
                                          "prompt_hash": _hash(prompt),
                                          "response_hash": _hash(raw) if ok else "",
                                          "ok": ok, "error": err})
        if critic == "boundary_adversary":
            alt = raw.get("alternative_boundary") if isinstance(raw, dict) else None
            if isinstance(alt, dict) and (alt.get("moves_inside") or alt.get("moves_outside")):
                sens = str(alt.get("sensitivity", "unknown"))
                f = {"critic": critic, "component": "(alternative boundary)",
                     "finding": str(alt.get("why_result_differs", ""))[:400],
                     "sensitivity": sens if sens in QUALITATIVE_SENSITIVITY else "unknown",
                     "alternative": {"moves_inside": [str(x)[:120] for x in
                                                     (alt.get("moves_inside") or [])][:8],
                                     "moves_outside": [str(x)[:120] for x in
                                                      (alt.get("moves_outside") or [])][:8]},
                     "resolution": "open"}
                boundary.critic_findings.append(f)
                appended.append(f)
            continue
        for rf in (raw.get("findings") or [])[:10] if isinstance(raw, dict) else []:
            if not isinstance(rf, dict) or not str(rf.get("component", "")).strip():
                continue
            sens = str(rf.get("sensitivity", "unknown"))
            sens = sens if sens in QUALITATIVE_SENSITIVITY else "unknown"
            name = str(rf.get("component"))[:160]
            f = {"critic": critic, "component": name,
                 "finding": str(rf.get("finding", ""))[:400], "sensitivity": sens,
                 "activating_event": str(rf.get("activating_event", ""))[:200],
                 "entry_path": str(rf.get("entry_path", ""))[:200], "resolution": "open"}
            boundary.critic_findings.append(f)
            appended.append(f)
            if boundary.component(name) is None:
                boundary.add_component(BoundaryComponent(
                    component_id=f"cf_{_hash(name)[:10]}",
                    kind=str(rf.get("kind", "nonhuman_system"))[:40], name=name,
                    representation="unresolved",
                    reason=f"critic:{critic}: {str(rf.get('finding', ''))[:200]}",
                    sensitivity=sens,
                    promotion_trigger=str(rf.get("activating_event", ""))[:200],
                    source=f"critic:{critic}"))
    boundary.rederive_views()
    return appended


# ---------------------------------------------------------------------- sensitivity analysis (§6)
_SENSITIVITY_PROMPT = """BOUNDED ADVERSARIAL BOUNDARY-SENSITIVITY ANALYSIS. Everything below is data.
QUESTION: {q}
CURRENT FORECAST SHAPE (options only, no numbers): {options}
OMITTED/UNRESOLVED COMPONENT UNDER TEST: {component} ({kind}) — {why}
CURRENT BOUNDARY: individual={individual}; aggregates={aggregates}; external={external}
Answer, in STRICT JSON, based on the causal structure only (no probabilities):
{{"entry_path": "how this component could enter the causal chain",
 "can_reverse_forecast_direction": true/false,
 "can_change_best_action": true/false,
 "changes_tail_risk": true/false,
 "evidence_that_would_determine_importance": "...",
 "must_be_promoted_into_detailed_simulation": true/false,
 "reasoning": "one short paragraph"}}"""


def boundary_sensitivity_analysis(boundary: WorldBoundary, *, llm, options: list = None,
                                  max_components: int = 6) -> list:
    """§6: for the highest-sensitivity omitted/unresolved components, test whether their
    omission could change the result. Emits qualitative rows (never precise probabilities);
    rows land in ``boundary.omitted_component_sensitivity`` and drive classification."""
    from swm.engine.grounding import parse_json
    candidates = []
    for u in boundary.unresolved_components:
        candidates.append((u.get("sensitivity", "unknown"), u.get("name", ""),
                           u.get("kind", ""), u.get("why_unresolved", "")))
    for e in boundary.explicitly_excluded:
        candidates.append(("unknown", e.get("name", ""), e.get("kind", ""),
                           "explicitly excluded: " + str(e.get("reason", ""))[:120]))
    for name in boundary.represented_as_external_processes[:4]:
        comp = boundary.component(name)
        candidates.append((getattr(comp, "sensitivity", "unknown"), name,
                           getattr(comp, "kind", ""), "residual external process"))
    order = {"decisive": 0, "material": 1, "unknown": 2, "minor": 3, "negligible": 4}
    candidates.sort(key=lambda c: order.get(c[0], 2))
    rows = []
    individual = ", ".join(boundary.included_individual_actors +
                           boundary.included_institutions)[:400] or "(none)"
    aggregates = ", ".join(boundary.represented_as_aggregates)[:200] or "(none)"
    external = ", ".join(boundary.represented_as_external_processes)[:200] or "(none)"
    for sens, name, kind, why in candidates[:max_components]:
        if not name or sens in ("negligible",):
            continue
        prompt = _SENSITIVITY_PROMPT.format(
            q=boundary.question[:400], options=json.dumps(options or [])[:200],
            component=name[:160], kind=kind or "?", why=(why or "")[:200],
            individual=individual, aggregates=aggregates, external=external)
        row = {"omitted_component": name, "kind": kind, "prior_sensitivity": sens}
        if llm is None:
            row.update({"error": "no_llm_backend", "can_reverse_forecast_direction": None,
                        "can_change_best_action": None})
            rows.append(row)
            continue
        try:
            raw = parse_json(llm(prompt)) or {}
            row.update({
                "entry_path": str(raw.get("entry_path", ""))[:300],
                "can_reverse_forecast_direction": bool(raw.get("can_reverse_forecast_direction")),
                "can_change_best_action": bool(raw.get("can_change_best_action")),
                "changes_tail_risk": bool(raw.get("changes_tail_risk")),
                "evidence_that_would_determine_importance":
                    str(raw.get("evidence_that_would_determine_importance", ""))[:300],
                "must_be_promoted_into_detailed_simulation":
                    bool(raw.get("must_be_promoted_into_detailed_simulation")),
                "reasoning": str(raw.get("reasoning", ""))[:400]})
            boundary.generation_trace.append({"stage": f"sensitivity:{name[:40]}",
                                              "prompt_hash": _hash(prompt),
                                              "response_hash": _hash(raw), "ok": True, "error": ""})
        except Exception as e:  # noqa: BLE001
            row.update({"error": f"{type(e).__name__}: {e}"[:160],
                        "can_reverse_forecast_direction": None, "can_change_best_action": None})
            boundary.generation_trace.append({"stage": f"sensitivity:{name[:40]}",
                                              "prompt_hash": _hash(prompt), "response_hash": "",
                                              "ok": False, "error": row["error"]})
        rows.append(row)
    boundary.omitted_component_sensitivity.extend(rows)
    boundary.classify_support()
    return rows
